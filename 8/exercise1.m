% 定义信号 x(n) = cos(0.48*pi*n) + cos(0.52*pi*n)
n1 = 0:9;
x1 = cos(0.48*pi*n1) + cos(0.52*pi*n1);

n2 = 0:99;
x2 = cos(0.48*pi*n2) + cos(0.52*pi*n2);

x3 = [x1, zeros(1, 90)];

% 计算幅度谱
X1 = abs(fft(x1));
X2 = abs(fft(x2));
X3 = abs(fft(x3));

% 绘制幅度谱
figure;

subplot(3, 1, 1);
stem(n1, X1);
title('幅度谱: 0 <= n < 10');

subplot(3, 1, 2);
stem(n2, X2); 
title('幅度谱: 0 <= n < 100');

subplot(3, 1, 3);
stem(0:99, X3);
title('[x(0), x(1), ..., x(9), zeros(1, 90)] 的幅度谱');

% 标签
xlabel('频率');
ylabel('幅度');

% legend('X1', 'X2', 'X3');

% 显示图例
legend('0 <= n < 10', '0 <= n < 100', '[x(0), x(1), ..., x(9), zeros(1, 90)]');

% 根据运行结果和理解给出高密度谱和高分辨谱的定义
%{
高密度谱： 高密度谱是指对信号进行零填充，使之成为一个更长的序列，然后计算其幅度谱。
高密度谱能够提供更密集的频率信息，但并没有增加对信号频率成分的分辨率。在上述代码中，第三个情况对应于高密度谱。

高分辨谱： 高分辨谱是指对信号进行更长时间的采样，然后计算其幅度谱。
高分辨谱通过在更长的时间范围内采样信号，能够提供更高的频率分辨率，更好地反映信号的真实频率成分。在上述代码中，第二个情况对应于高分辨谱。
%}